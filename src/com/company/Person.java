package com.company;

public class Person {
   private String name;
   private int age;
   private double rost;
   private short ves;
   private int godRojenie;

   public String getName(){
       return name;
   }
   public void setName(String name){
       this.name=name;
   }
   public int getAge(){
       return age;
   }
   public void setAge(int age){
       this.age=age;
   }
   public double getRost(){
       return rost;
   }
   public void setRost(double rost){
       this.rost=rost;
   }
   public short getVes(){
       return ves;
   }
   public void setVes(short ves){
       this.ves=ves;
   }
   public int getGodRojenie(){
       return godRojenie;
   }
   public void setGodRojenie(int godRojenie){
       this.godRojenie=godRojenie;
   }

}
